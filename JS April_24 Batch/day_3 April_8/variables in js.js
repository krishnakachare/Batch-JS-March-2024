// Keywords in js for variable declaration: var, let, const

// What is differnce bt var, let const?

// 1. var:

// From js beginig.
// Scope: global, function scope.
// Can access variable before the initialization. (TDZ)
// Can reassign variable value.
// can redeclare variable
// Initialization not mandatory


// 2. let:

// ES6 feature
// Scope: global, function, block/script scope.
// Can not access variable before the initialization. We have TypeError (TDZ)
// Can reassign variable value.
// can not redeclare variable
// Initialization not mandatory

// 3. const:

// ES6 feature
// Scope: global, function, block/script scope.
// Can not access variable before the initialization. We have TypeError (TDZ)
// Can not reassign/change/update variable value.
// can not redeclare variable
// Initialization mandatory







